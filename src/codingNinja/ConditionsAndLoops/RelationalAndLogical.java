package codingNinja.ConditionsAndLoops;

public class RelationalAndLogical {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<=b && b>10);
        System.out.println(a<=b || b>10);
        System.out.println(!(a>b));
    }
}
