package com.vamsi;

public class ShortHandAssignment {
    public static void main(String[] args) {
        int number = 2;
        int numberOne = 10;

        System.out.println(number += 2);
        System.out.println(numberOne -= 2);
        System.out.println(numberOne *= 2);
        System.out.println(numberOne /= 2);
        System.out.println(numberOne %= 2);
    }
}
