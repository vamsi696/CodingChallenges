package com.vamsi;

public class Comparison {
    public static void main(String[] args) {

        System.out.println(10 < 10);
        System.out.println(10 <= 10);
        System.out.println(10 > 10);
        System.out.println(10 >= 10);
        System.out.println(10 == 10);
        System.out.println(10 != 10);

        System.out.println();
        boolean isAdult = 18 >= 18;
        System.out.println(isAdult);
    }
}
